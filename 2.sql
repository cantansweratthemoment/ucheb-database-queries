select "Н_ЛЮДИ"."ИМЯ", "Н_ОБУЧЕНИЯ"."НЗК", "Н_УЧЕНИКИ"."НАЧАЛО"
from "Н_ЛЮДИ"
         left join "Н_ОБУЧЕНИЯ" on ("Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД")
         left join "Н_УЧЕНИКИ" on ("Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД")
where "Н_ЛЮДИ"."ФАМИЛИЯ" > 'Ёлкин'
  and "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" > 112514
  and "Н_УЧЕНИКИ"."ИД" > 1;